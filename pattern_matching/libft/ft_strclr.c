#include <stdlib.h>
#include "libft.h"

void	ft_strclr(char *s)
{
	size_t	i;
	size_t	len;

	i = 0;
	if (s != NULL)
	{
		len = ft_strlen(s);
		while (i < len && *s)
		{
			*s = '\0';
			s++;
		}
	}
}
