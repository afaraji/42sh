#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t	len;
	size_t	i;

	i = 0;
	if (s != NULL && f != NULL)
	{
		len = ft_strlen(s);
		while (i < len && *s != '\0')
		{
			(*f)(s + i);
			i++;
		}
	}
}
