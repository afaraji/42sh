#include "libft.h"

void	ft_lstprint(t_list *lst, void (*f)(void *p))
{
	while (lst)
	{
		write(1, "\n---->", 6);
		f(lst->content);
		lst = lst->next;
	}
	write(1, "\n*NULL*\n", 8);
}
